$base --rows 150 --columns 200 --targets 3 --output "$output_dir/highest_parent_4c/150_200/"
echo "---"
$base --rows 100 --columns 200 --targets 3 --output "$output_dir/highest_parent_4c/100_200/"
echo "---"
$base --rows 50 --columns 200 --targets 3 --output "$output_dir/highest_parent_4c/50_200/"

