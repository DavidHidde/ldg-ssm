$base --rows 200 --columns 200 --targets 2 --output "$output_dir/aggregate_hierarchy_4c/200_200/"
echo "---"
$base --rows 150 --columns 200 --targets 2 --output "$output_dir/aggregate_hierarchy_4c/150_200/"
echo "---"
$base --rows 100 --columns 200 --targets 2 --output "$output_dir/aggregate_hierarchy_4c/100_200/"
echo "---"
$base --rows 50 --columns 200 --targets 2 --output "$output_dir/aggregate_hierarchy_4c/50_200/"

