#!/bin/bash
executable=$1 # First arg: The executable location
output_dir=$2 # Second arg: The output directory

base="$executable --debug  --seed 1"

echo "########### Aggregate Hierarchy ###########"
$base --rows 200 --columns 200 --targets 0 --output "$output_dir/aggregate_hierarchy/200_200/"
echo "---"
$base --rows 150 --columns 200 --targets 0 --output "$output_dir/aggregate_hierarchy/150_200/"
echo "---"
$base --rows 100 --columns 200 --targets 0 --output "$output_dir/aggregate_hierarchy/100_200/"
echo "---"
$base --rows 50 --columns 200 --targets 0 --output "$output_dir/aggregate_hierarchy/50_200/"

echo "########### Highest parent ###########"
$base --rows 200 --columns 200 --targets 1 --output "$output_dir/highest_parent/200_200/"
echo "---"
$base --rows 150 --columns 200 --targets 1 --output "$output_dir/highest_parent/150_200/"
echo "---"
$base --rows 100 --columns 200 --targets 1 --output "$output_dir/highest_parent/100_200/"
echo "---"
$base --rows 50 --columns 200 --targets 1 --output "$output_dir/highest_parent/50_200/"

